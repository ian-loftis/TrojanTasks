package jsonObjects;

public class RequestTask {
	String name;
	String description;
	Integer ID;
	boolean completed;
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setDescription(String description)
	{
		this.description = description;
	}
	
	public String getDescription()
	{
		return description;
	}
}
